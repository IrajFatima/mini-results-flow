import { motion } from "framer-motion";
import {
    FaAppleAlt,
    FaTint,
    FaRunning,
    FaBed,
    FaBullseye,
    FaCheckCircle,
    FaExclamationTriangle,
    FaRobot,
} from "react-icons/fa";
import type { JSX } from "react/jsx-runtime";
import type { AIRecommendation } from "../../types/aiRecommendation";
import { useEffect, useState } from "react";
import { getAIRecommendations } from "../../services/ai";

const iconMap: Record<string, JSX.Element> = {
    Nutrition: <FaAppleAlt className="text-2xl text-green-500" />,
    Hydration: <FaTint className="text-2xl text-sky-500" />,
    Exercise: <FaRunning className="text-2xl text-orange-500" />,
    Recovery: <FaBed className="text-2xl text-purple-500" />,
    Consistency: <FaBullseye className="text-2xl text-red-500" />,
};

export default function AIRecommendationsPage() {
    const [report, setReport] = useState<AIRecommendation | null>(null);
    const [loading, setLoading] = useState(true);

    useEffect(() => {
        const loadRecommendations = async () => {
            try {
                const response = await getAIRecommendations();
                // If backend returns a JSON string
                setReport(response);
            } catch (error) {
                console.error(error);
            } finally {
                setLoading(false);
            }
        };

        loadRecommendations();
    }, []);
    if (loading) {
        return (
            <div className="min-h-screen flex items-center justify-center">
                <div className="flex flex-col items-center gap-5">

                    <div className="w-14 h-14 border-4 border-[#36BC9F] border-t-transparent rounded-full animate-spin" />

                    <h2 className="text-2xl font-semibold">
                        Preparing your AI report...
                    </h2>

                    <p className="text-gray-500">
                        This may take a few seconds.
                    </p>

                </div>
            </div>
        );
    }
    if (!report) {
        return (
            <div className="min-h-screen flex items-center justify-center">

                <div className="text-center">

                    <FaRobot className="mx-auto text-6xl text-[#36BC9F]" />

                    <h2 className="mt-5 text-3xl font-bold">
                        AI Report Unavailable
                    </h2>

                    <p className="mt-2 text-gray-500">
                        Please try again later or Submit form to get Report
                    </p>

                </div>

            </div>
        );
    }
    const assessment = {
        Excellent: {
            color: "bg-green-100 text-green-700",
            emoji: "🟢",
        },
        Good: {
            color: "bg-green-100 text-green-700",
            emoji: "✅",
        },
        Fair: {
            color: "bg-yellow-100 text-yellow-700",
            emoji: "🟡",
        },
        "Needs Improvement": {
            color: "bg-red-100 text-red-700",
            emoji: "🔴",
        },
    }[report.overallAssessment] ?? {
        color: "bg-gray-100",
        emoji: "ℹ️",
    };
    return (
        <div className="min-h-screen bg-[#F8F4F4] dark:bg-[#1b1d1e] py-10">
            <div className="max-w-6xl mx-auto px-4">

                {/* Header */}

                <motion.div
                    initial={{ opacity: 0, y: -30 }}
                    animate={{ opacity: 1, y: 0 }}
                    className="rounded-3xl bg-[#36BC9F] text-white p-10 shadow-xl"
                >
                    <div className="flex items-center gap-4">

                        <FaRobot size={45} />

                        <div>
                            <h1 className="text-4xl font-bold">
                                {report.title}
                            </h1>

                            <p className="opacity-90 mt-2">
                                AI Generated Personalized Health Report
                            </p>
                            <p className="opacity-90 mt-2">
                                Generated by AI • {new Date().toLocaleDateString()}
                            </p>
                        </div>

                    </div>
                </motion.div>

                {/* Summary */}

                <motion.div
                    initial={{ opacity: 0 }}
                    animate={{ opacity: 1 }}
                    transition={{ delay: .15 }}
                    className="bg-white dark:bg-[#232627] rounded-2xl p-8 mt-8 shadow"
                >

                    <div className="flex justify-between items-center">

                        <h2 className="text-2xl font-bold text-[#183B49] dark:text-white">
                            Summary
                        </h2>

                        <span className={`px-4 py-2 rounded-full font-semibold ${assessment.color}`}>
                            {assessment.emoji} {report.overallAssessment}
                        </span>

                    </div>

                    <p className="mt-5 text-lg leading-8 text-gray-600 dark:text-gray-300">
                        {report.summary}
                    </p>

                </motion.div>

                {/* Strengths */}

                <div className="grid lg:grid-cols-2 gap-6 mt-8">

                    <div className="bg-white dark:bg-[#232627] rounded-2xl p-7 shadow">

                        <h3 className="text-xl font-bold flex items-center gap-3 mb-5 text-green-600">

                            <FaCheckCircle />

                            Strengths

                        </h3>

                        <ul className="space-y-4">

                            {report.strengths.map((item, i) => (

                                <li key={i} className="flex gap-3">

                                    <FaCheckCircle className="text-green-500 mt-1" />

                                    <span>{item}</span>

                                </li>

                            ))}

                        </ul>

                    </div>

                    <div className="bg-white dark:bg-[#232627] rounded-2xl p-7 shadow">

                        <h3 className="text-xl font-bold flex items-center gap-3 mb-5 text-red-600">

                            <FaExclamationTriangle />

                            Areas To Improve

                        </h3>

                        <ul className="space-y-4">

                            {report.areasToImprove.map((item, i) => (

                                <li key={i} className="flex gap-3">

                                    <FaExclamationTriangle className="text-red-500 mt-1" />

                                    <span>{item}</span>

                                </li>

                            ))}

                        </ul>

                    </div>

                </div>

                {/* Sections */}

                <div className="grid md:grid-cols-2 gap-6 mt-8">

                    {report.sections.map((section, index) => (

                        <motion.div
                            key={index}
                            initial={{ opacity: 0, y: 30 }}
                            whileInView={{ opacity: 1, y: 0 }}
                            viewport={{ once: true }}
                            className="bg-white dark:bg-[#232627] rounded-2xl p-7 shadow"
                        >

                            <div className="flex items-center gap-4">

                                {iconMap[section.title]}

                                <h3 className="text-xl font-bold">
                                    {section.title}
                                </h3>

                            </div>

                            <p className="mt-5 leading-8 text-gray-600 dark:text-gray-300">

                                {section.content}

                            </p>

                        </motion.div>

                    ))}

                </div>

                {/* Weekly Goals */}

                <div className="bg-white dark:bg-[#232627] rounded-2xl p-8 shadow mt-8">

                    <h2 className="text-2xl font-bold mb-6">
                        Weekly Goals
                    </h2>

                    <div className="space-y-5">

                        {report.weeklyGoals.map((goal, index) => (

                            <div
                                key={index}
                                className="flex items-start gap-4"
                            >

                                <div className="mt-1 h-6 w-6 rounded border-2 border-[#36BC9F]" />

                                <p>{goal}</p>

                            </div>

                        ))}

                    </div>

                </div>

                {/* Motivation */}

                <div className="mt-8 rounded-3xl bg-gradient-to-r from-[#36BC9F] to-[#2ca98e] p-10 text-white shadow-xl">

                    <h2 className="text-3xl font-bold mb-4">

                        Keep Going 🚀

                    </h2>

                    <p className="text-lg leading-9">

                        {report.motivation}

                    </p>

                </div>

            </div>
        </div>
    );
}